    private void retypePassKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_retypePassKeyReleased
        // TODO add your handling code here:
        String registerPassword = new String (password.getPassword());
        String retypePassword = new String (retypePass.getPassword());

        if (registerPassword.equals(retypePassword)) {
            cekPass.setText("Match");
        } else if (!registerPassword.equals(retypePassword)) {
            cekPass.setText("Not Match");
        }
    }//GEN-LAST:event_retypePassKeyReleased

    private void btnDaftarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDaftarActionPerformed
        // radio button jenis kelamin
        String jenisKelamin = "";

        if ( rbLaki.isSelected() ) {
            jenisKelamin = rbLaki.getText();
        } else if ( rbPerempuan.isSelected() ){
            jenisKelamin = rbPerempuan.getText();
        }

        String sql = "INSERT INTO user VALUES (null,?,?,?,?,?,?,?)";

        try {

            if(nama.getText().equals("")||username.getText().equals("")||password.getText().equals("")
                ||cmbLevel.getSelectedItem().equals("----PILIH LEVEL----")
                ||alamat.getText().equals("")||phone.getText().equals(""))
            {
                JOptionPane.showMessageDialog(null, "Masukkan data dengan benar !","Kesalahan", JOptionPane.ERROR_MESSAGE);
                return;
            } else {
                ps = conn.prepareStatement(sql);

                ps.setString(1, nama.getText());
                ps.setString(2, username.getText());
                ps.setString(3, password.getText());
                ps.setString(4, cmbLevel.getSelectedItem().toString());
                ps.setString(5, jenisKelamin);
                ps.setString(6, alamat.getText());
                ps.setString(7, phone.getText());

                ps.executeUpdate();
                JOptionPane.showMessageDialog(rootPane, "Berhasil Mendaftar");

                // masuk ke halaman login
                HalamanLogin move = new HalamanLogin();
                move.setVisible(true);
                dispose();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_btnDaftarActionPerformed

